package exceptions;

/**
 * This class aims to represent the exception ObjectNotCreated.
 * This exception is triggered if the request could not be carried out because of intermediate objects not being created
 *
 * @author LAMA.
 * @version 1.
 */
public class ObjectNotCreated extends Exception {

    /**
     * Constructor of the class.
     */
    public ObjectNotCreated() {
        super();
    }

}
